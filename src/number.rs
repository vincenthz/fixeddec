use core::ops::{Add, AddAssign, Div, DivAssign, Mul, MulAssign, Sub, SubAssign};

pub trait Number:
    Copy
    + Add<Output = Self>
    + AddAssign
    + Sub<Output = Self>
    + SubAssign
    + Mul<Output = Self>
    + MulAssign
    + Div<Output = Self>
    + DivAssign
    + alloc::fmt::Debug
    + alloc::fmt::Display
    + 'static
{
    const TEN_POWER: &'static [Self];

    /// Give the power of 10s for this type
    ///
    /// This should be equivalent to:
    ///
    /// ```ignore
    /// fn self_ten_power<T>(p: u32) -> Option<T> {
    ///     let (r, overflowed) = T::overflowing_pow(10, p);
    ///     (!overflowed).then_some(r)
    /// }
    /// ```
    fn ten_power(p: u32) -> Option<Self>;
    fn checked_add(self, rhs: Self) -> Option<Self>;
    fn checked_sub(self, rhs: Self) -> Option<Self>;
    fn checked_mul(self, rhs: Self) -> Option<Self>;
    fn checked_div(self, rhs: Self) -> Option<Self>;
    fn checked_rem(self, rhs: Self) -> Option<Self>;
}

macro_rules! number_impl {
    ($ty:ty, $power10:expr, $($tt:tt)+) => {
        impl Number for $ty {
            const TEN_POWER : &'static [$ty] = &$power10;
            fn ten_power(p: u32) -> Option<Self> {
                Self::TEN_POWER.get(p as usize).cloned()
            }
            fn checked_add(self, rhs: $ty) -> Option<$ty> {
                self.checked_add(rhs)
            }
            fn checked_sub(self, rhs: $ty) -> Option<$ty> {
                self.checked_sub(rhs)
            }
            fn checked_mul(self, rhs: $ty) -> Option<$ty> {
                self.checked_mul(rhs)
            }
            fn checked_div(self, rhs: $ty) -> Option<$ty> {
                self.checked_div(rhs)
            }
            $($tt)+
        }
    };
}

macro_rules! number_unsigned_impl {
    ($ty:ty,$power10:expr) => {
        number_impl!(
            $ty,
            $power10,
            fn checked_rem(self, rhs: $ty) -> Option<$ty> {
                self.checked_rem(rhs)
            }
        );
    };
}
macro_rules! number_signed_impl {
    ($ty:ty,$power10:expr) => {
        number_impl!(
            $ty,
            $power10,
            fn checked_rem(self, rhs: $ty) -> Option<$ty> {
                self.abs().checked_rem(rhs)
            }
        );
    };
}

